#include <bits/stdc++.h>

using namespace std;

int main()
{
    int l, a, b, c, d;
    cin >> l >> a >> b >> c >> d;
    int ma, mb;
    ma = a / c;
    if (a % c != 0) ma++;
    mb = b / d;
    if (b % d != 0) mb++;
    cout << l - max(ma, mb);
    return 0;
}